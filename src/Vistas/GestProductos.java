    public GestProductos() {
        initComponents();

        FormuEditar.hide();
        crud = new CRUDProduc();

        try {
            DefaultTableModel table = new DefaultTableModel();
            tblProductos.setModel(table);

            PreparedStatement prep = null;
            ResultSet resul = null;

            String sql = "SELECT Id, Nombre, Marca, Categoría, Precio, Cantidad_Disponible FROM registroproductos ";

            prep = conec.prepareStatement(sql);
            resul = prep.executeQuery();

            ResultSetMetaData rsd = resul.getMetaData();
            int cantColum = rsd.getColumnCount();

            table.addColumn("Id");
            table.addColumn("Nombre");
            table.addColumn("Marca");
            table.addColumn("Categoría");
            table.addColumn("Precio");
            table.addColumn("Cantidad disponible");

            while (resul.next()) {

                Object[] filas = new Object[cantColum];

                for (int i = 0; i < cantColum; i++) {

                    filas[i] = resul.getObject(i + 1);
                }

                table.addRow(filas);

            }
            this.dispose();

        } catch (SQLException ex) {

            System.err.println(ex.toString());

        }

    }

