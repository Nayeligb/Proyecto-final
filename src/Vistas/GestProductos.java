    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblProductos.getSelectedRowCount();
        if (fila < 1) {
            JOptionPane.showMessageDialog(null, "Seleccione producto a eliminar");
        } else {
            if (crud.Eliminar(tblProductos.getValueAt(tblProductos.getSelectedRow(), 0).toString()) > 0) {
                limpiar();
                mostrar();
                FormuEditar.hide();
            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        String id = txtId.getText();
        String nombre = txtProducto.getText();
        String marca = txtMarca.getText();
        String categoria = txtCategoria.getText();
        String precio = txtPrecio.getText();
        String cantidad = txtCantidad.getText();
        crud.Editar(id, nombre, marca, categoria, precio, cantidad);
        mostrar();
        limpiar();
        FormuEditar.hide();
    }//GEN-LAST:event_btnModificarActionPerformed

